int main()
{
	ios::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	int n;
	cin >> n;
	int ret = 1;
	if (1 == n)
	{
		cout << 1;
		return 0;
	}
	--n;
	int cur = 6;
	do
	{
		n -= cur;
		cur += 6;
		++ret;
	} while (0 < n);

	cout << ret;
	return 0;
}
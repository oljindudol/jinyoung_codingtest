template <typename T>
void PrintVec(const vector<T>& vec)
{
	for (const auto& e : vec)
	{
		cout << e << " ";
	}
	cout << '\n';
}


template <typename T>
void PrintVec(const vector<vector<T>>& vec)
{
	for (const auto& e1 : vec)
	{
		for (const auto& e2 : e1)
		{
			cout << e2 << " ";
		}
		cout << '\n';
	}
	cout << '\n';
}

/////////////////////////////////////////////

int main()
{
	ios::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	int a, b, c;

	cin >> a >> b >> c;

	int p = a * b * c;
	vector<int> nums(10);
	string s = to_string(p);

	for (const auto& e : s)
	{
		++nums[e - '0'];
	}

	for (int i = 0; i < 10; ++i)
	{
		cout << nums[i] << " ";
	}

	return 0;
}

